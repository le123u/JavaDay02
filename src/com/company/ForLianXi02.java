public class ForLianXi02 {
    public static void main(String[] args) {
        // 通过for循环, 打印1~5之间的数字
        for ( int i = 1; i <= 5; i++){
            System.out.println(i);
        }
        // 通过for循环, 打印5~1之间的数字
        for (int i = 5; i > 0; i--){
            System.out.println(i);
        }
    }

}
