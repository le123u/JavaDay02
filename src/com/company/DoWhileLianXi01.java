public class DoWhileLianXi01 {
    public static void main(String[] args) {
        //通过do.while循环, 在控制台打印10次Hello World!
        int i = 1;
        do {
            System.out.println("HelloWorld");
            i++;
        }while(i <= 10);
    }
}
